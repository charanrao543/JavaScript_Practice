/*Datatypes in js and methods used to check datatypes in js:
1.number
2.string
3.boolean
4.null
5.undefined
6.array
7.object */
//Note:To check of variable we have to make use of method "typeof()"

//1.number datatype:All the whole and real numnber will come under the number datatype only
var n=10
console.log(n) //10
console.log(typeof(n)) //number

var n=20000000000.678
console.log(n) //20000000000.678
console.log(typeof(n)) //number

var n=-278794879283
console.log(n) //-278794879283
console.log(typeof(n)) //number

//2.string datatype: in js single charater or series of character will come under string datatype only
var a="k"
console.log(a) //k
console.log(typeof(a)) //string


var a="kodnest"
console.log(a) //kodnest
console.log(typeof(a)) //string

//3.boolean datatype:
var b=true
console.log(b) //true
console.log(typeof(b)) //boolean

var b=false
console.log(b) //false
console.log(typeof(b)) //boolean


//4.null datatype:
var c=null
console.log(c) //null
console.log(typeof(c)) //object

//5.undefined datatype:
var d
console.log(d) //undefined
console.log(typeof(d)) //undefined

//6.array
var e=[10,20,30,40]
console.log(e) //[10,20,30,40]
console.log(typeof(e)) //object






